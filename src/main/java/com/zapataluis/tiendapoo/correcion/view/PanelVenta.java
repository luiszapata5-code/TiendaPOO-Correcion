    private void btnCarritoVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarritoVActionPerformed
      
        try {
            int idUsuario = Integer.parseInt(txtId.getText().trim());
            int cantidad = Integer.parseInt(txtCantidad.getText().trim());
            String nombre = txtNombreProducto.getText().trim();

           clienteActual = clienteServicio.buscarClientePorId(idUsuario);
           Producto p = productoServicio.buscarProductoPorNombre(nombre);
           
           
           
           if (clienteActual == null) {
              JOptionPane.showMessageDialog(this, "Cliente no encontrado.");
               return;
            }

           
             if (p== null) {
             JOptionPane.showMessageDialog(this, "Producto no encontrado.");
             return;
            }

           // Verificar stock acumulado en carrito (bug de doble reserva)
           int yaEnCarrito = carritoTemporal.stream()
           .filter(d -> d.getProducto().getId() == p.getId())
           .mapToInt(DetalleVenta::getCantidad)
           .sum();
           if (cantidad + yaEnCarrito > p.getStock()) {
             JOptionPane.showMessageDialog(this, "Stock insuficiente. Ya tienes " + yaEnCarrito + " en carrito.");
             return;
           }

            carritoTemporal.add(new DetalleVenta(p, cantidad)); 
            actualizarTablaCarrito();
            txtCantidad.setText("");
            txtNombreProducto.setText("");
            JOptionPane.showMessageDialog(this, "Producto agregado al carrito.");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID y cantidad deben ser números válidos.");
        }
    }//GEN-LAST:event_btnCarritoVActionPerformed

    private void btnRegistrarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVActionPerformed
          
        if (clienteActual == null) {
            JOptionPane.showMessageDialog(this, "Primero busca un cliente por ID.");
            return;
        }
        if (carritoTemporal.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El carrito está vacío. Agrega productos primero.");
            return;
        }

        boolean exito = ventaServicio.registrarVenta(clienteActual, carritoTemporal);
        if (exito) {
            JOptionPane.showMessageDialog(this, "Venta registrada correctamente.");
            cargarTablaVentas();
            limpiarVenta();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar la venta. Verifica el stock.");
        }
                                
    }//GEN-LAST:event_btnRegistrarVActionPerformed

