    public void cargarTablaVentas() {
        modeloVentas.setRowCount(0);
        for (Venta v : ventaServicio.getVentas()) {
            Object[] fila = {
                v.getId(),
                v.getCliente().getNombre(),
                v.getSobreLaVenta().size() + " producto(s)",
                "$" + v.getTotal()
            };
            modeloVentas.addRow(fila);
        }
    }

    // Actualiza la tabla del carrito con los productos temporales
    private void actualizarTablaCarrito() {
        modeloCarrito.setRowCount(0);
        for (DetalleVenta s : carritoTemporal) {
            Producto p = s.getProducto();
            double subtotal = p.getPrecio() * s.getCantidad();
            Object[] fila = {
                p.getNombre(),
                p.getTalla(),
                p.getColor(),
                p.getPrecio(),
                s.getCantidad(),
                subtotal
            };
            modeloCarrito.addRow(fila);
        }
    }

    private void limpiarVenta() {
        carritoTemporal.clear();
        clienteActual = null;
        txtId.setText("");
        txtCantidad.setText("");
        txtNombreProducto.setText("");
        actualizarTablaCarrito();
    }

